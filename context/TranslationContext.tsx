interface TranslationContextType {
  language: SupportedLanguage;
  setLanguage: (lang: SupportedLanguage) => void;
  translate: (text: string) => Promise<string>;
  availableLanguages: typeof availableLanguages;
}

const TranslationContext = createContext<TranslationContextType | undefined>(undefined);

export const useTranslation = () => {
  const context = useContext(TranslationContext);
  if (!context) {
    throw new Error('useTranslation must be used within a TranslationProvider');
  }
  return context;
};

interface TranslationProviderProps {
  children: ReactNode;
}

export const TranslationProvider: React.FC<TranslationProviderProps> = ({ children }) => {
  const [language, setLanguageState] = useState<SupportedLanguage>('en');
  const [translating, setTranslating] = useState<Set<string>>(new Set());

  // 从localStorage恢复语言设置
  useEffect(() => {
    const savedLang = localStorage.getItem('tinytech_language') as SupportedLanguage;
    if (savedLang && availableLanguages.some(l => l.code === savedLang)) {
      setLanguageState(savedLang);
    }
  }, []);

  // 设置语言
  const setLanguage = useCallback((lang: SupportedLanguage) => {
    setLanguageState(lang);
    localStorage.setItem('tinytech_language', lang);
  }, []);

  // 翻译函数 - 仅使用本地字典
  const translate = useCallback(async (text: string): Promise<string> => {
    // 如果文本为空，直接返回
    if (!text || !text.trim()) return text;
    
    // 如果是英语，直接返回
    if (language === 'en') return text;

    console.log(`[Translation] Looking up "${text}" in local dictionary for ${language}`);

    // 检查本地字典
    const dict = localDict[language];
    if (dict) {
      // 1. 优先尝试用原始 key 查找（如 "Home"）
      const lowerKey = text.toLowerCase().slice(0, 100);
      if (dict[lowerKey]) {
        console.log(`[Translation] Found in local dict: ${dict[lowerKey]}`);
        return dict[lowerKey];
      }
      
      // 2. 如果 key 包含空格，尝试用空格前的词作为前缀（如 "Home" -> "nav.home"）
      const firstSpace = text.indexOf(' ');
      const mainKey = firstSpace > 0 ? text.substring(0, firstSpace) : text;
      const keyWithNav = `nav.${mainKey.toLowerCase()}`;
      if (dict[keyWithNav]) {
        console.log(`[Translation] Found in local dict (with nav prefix): ${dict[keyWithNav]}`);
        return dict[keyWithNav];
      }
    }

    // 如果本地字典中没有找到，返回原文
    console.log(`[Translation] No translation found in local dictionary, returning original text: "${text}"`);
    return text;
  }, [language]);

  return (
    <TranslationContext.Provider value={{ 
      language, 
      setLanguage, 
      translate,
      availableLanguages
    }}>
      {children}
    </TranslationContext.Provider>
  );
};

// 同步翻译Hook - 立即返回fallback，后台异步更新
export function useTranslatedText(fallback: string): string {
  const { translate, language } = useTranslation();
  const [translated, setTranslated] = useState(fallback);

  useEffect(() => {
    if (language === 'en') {
      setTranslated(fallback);
      return;
    }

    let mounted = true;
    translate(fallback).then(result => {
      if (mounted) setTranslated(result);
    });

    return () => { mounted = false; };
  }, [fallback, language, translate]);

  return translated;
}

// 批量翻译Hook - 优化请求频率
export function useTranslatedTexts(fallbacks: string[]): string[] {
  const { translate, language } = useTranslation();
  const [translated, setTranslated] = useState(fallbacks);
  const [isLoading, setIsLoading] = useState(false);
  const mountedRef = useRef(true);
  const timeoutIdRef = useRef<ReturnType<typeof setTimeout> | null>(null);

  useEffect(() => {
    mountedRef.current = true;
    
    return () => {
      mountedRef.current = false;
      if (timeoutIdRef.current) {
        clearTimeout(timeoutIdRef.current);
      }
    };
  }, []);

  useEffect(() => {
    // 如果正在加载，跳过
    if (isLoading) {
      return;
    }

    // 如果是英语，直接使用原文
    if (language === 'en') {
      setTranslated(fallbacks);
      return;
    }

    // 使用 setTimeout 避免立即执行导致的无限循环
    timeoutIdRef.current = setTimeout(() => {
      if (!mountedRef.current) return;
      
      setIsLoading(true);
      const results = [...fallbacks];
      let currentIndex = 0;

      const processNext = async () => {
        if (currentIndex >= fallbacks.length || !mountedRef.current) {
          if (mountedRef.current) {
            setTranslated(results);
          }
          setIsLoading(false);
          return;
        }

        try {
          const result = await translate(fallbacks[currentIndex]);
          if (mountedRef.current) {
            results[currentIndex] = result;
          }
        } catch (err) {
          // 翻译失败时保持原文
          if (mountedRef.current) {
            results[currentIndex] = fallbacks[currentIndex];
          }
        }

        currentIndex++;
        
        // 增加延迟到1秒，大大减少API压力
        if (mountedRef.current) {
          timeoutIdRef.current = setTimeout(processNext, 1000);
        }
      };

      processNext();
    }, 100);

    return () => {
      if (timeoutIdRef.current) {
        clearTimeout(timeoutIdRef.current);
      }
    };
  }, [fallbacks.join(','), language, translate]);

  return translated;
}